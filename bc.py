  insulinGen = 6.00 + 0.025 * (prog5 * 314.46 * (10**9) / (10**3)) + 0.086 * (est5 * 272.38 * (10**3)) * (6 * 10**(-9)) * (10**9) / (10**3)
  ins10 = ins5 + (insulinGen * robFrac) # * (6.9444 / (10**9)
